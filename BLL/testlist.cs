		public List<Maticsoft.Model.testlist> DataTableToList(DataTable dt)
		{
			List<Maticsoft.Model.testlist> modelList = new List<Maticsoft.Model.testlist>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				Maticsoft.Model.testlist model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

